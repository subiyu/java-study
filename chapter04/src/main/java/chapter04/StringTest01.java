package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("C:\\temp");
		
		// "Hello"
		System.out.println("\"hello\"");
		
		/* 제어 문자 */
		// \t: tab
		// \r: carriage return
		// \n: newline
		// \b: beep
		
		System.out.print("hello\tworld\n");
		System.out.println("hello\tworld");
	}

}
